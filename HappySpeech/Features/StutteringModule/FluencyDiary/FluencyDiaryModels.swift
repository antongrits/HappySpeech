import Foundation

// MARK: - FluencyDiaryTexts
//
// Библиотека текстов для чтения вслух в дневнике плавности речи (модуль
// заикания). Тексты сгруппированы по трём уровням сложности и подобраны
// для плавного чтения: короткие фразы, простая лексика, без стечений
// согласных и скороговорочных конструкций, провоцирующих дисфлюенцию.

enum FluencyDiaryTexts {

    /// Уровень сложности текста для чтения вслух.
    enum Tier: Int, CaseIterable, Sendable {
        /// 5–6 лет: 2–3 коротких предложения, базовая лексика.
        case easy
        /// 6–7 лет: 3–4 предложения, повествование.
        case medium
        /// 7–8 лет: 4–5 предложений, связный сюжет.
        case advanced
    }

    static let easyPassages: [String] = [
        "Кот лежит на коврике. Он тихо мурлычет. Кот любит спать.",
        "Мама печёт пирог. Пирог пахнет вкусно. Мы будем пить чай.",
        "На небе светит солнце. Дует тёплый ветер. Мы идём гулять.",
        "У Ани есть мячик. Мячик синий и круглый. Аня катит мячик.",
        "Зайка сидит в траве. Зайка кушает морковку. Морковка сладкая.",
        "Папа моет машину. Машина стала чистой. Папа доволен.",
        "Птичка свила гнездо. В гнезде лежат яйца. Птичка поёт песню.",
        "Дима рисует море. Море синее и большое. По морю плывёт лодка."
    ]

    static let mediumPassages: [String] = [
        "Утром выпал снег. Дети вышли во двор. Они лепят снеговика. У снеговика морковка вместо носа.",
        "В лесу живёт ёжик. Днём ёжик спит под кустом. Ночью он ищет грибы и ягоды.",
        "Бабушка вяжет тёплые носки. Носки получаются мягкими. Зимой в них не холодно ногам.",
        "Маша посадила цветок. Каждый день она поливает его водой. Скоро цветок вырастет большим.",
        "Котёнок играет с клубком. Клубок катится по полу. Котёнок бежит за ним и весело прыгает.",
        "Летом мы ездили на дачу. Там есть пруд и зелёный сад. Мы собирали спелые яблоки.",
        "Дети читают книгу о животных. На картинках слон, жираф и тигр. Книга очень интересная.",
        "Дождик закончился, и вышло солнце. На небе появилась радуга. Она яркая и красивая."
    ]

    static let advancedPassages: [String] = [
        "Однажды утром Петя проснулся рано. За окном пели птицы, а в комнату заглядывало солнце. "
            + "Мальчик быстро оделся и побежал завтракать. Сегодня вся семья поедет в парк.",
        "В нашем дворе растёт высокий клён. Осенью его листья становятся жёлтыми и красными. "
            + "Ветер срывает листья, и они тихо падают на землю. Мы собираем самые красивые листочки.",
        "У Кати есть младший брат. Каждый вечер она рассказывает ему сказку. "
            + "Брат внимательно слушает и быстро засыпает. Катя тихо выходит из комнаты.",
        "Зимой река покрылась толстым льдом. Дети взяли коньки и пошли кататься. "
            + "Они весело смеялись и катались до самого вечера. Домой все вернулись румяными.",
        "Наша собака Дружок очень умная. Она умеет приносить мячик и подавать лапу. "
            + "Когда я прихожу из школы, Дружок радостно встречает меня у двери.",
        "Весной в саду расцвели первые цветы. Над ними кружат пчёлы и собирают сладкий нектар. "
            + "Скоро на деревьях появятся маленькие зелёные листочки.",
        "Летним днём мы пошли на рыбалку. Папа показал, как правильно держать удочку. "
            + "Я долго ждал и наконец поймал маленькую рыбку. Мы отпустили её обратно в реку.",
        "В цирке выступали весёлые артисты. Клоун смешил детей, а ловкий жонглёр подбрасывал кольца. "
            + "Больше всего мне понравились дрессированные собачки. Это был чудесный день."
    ]

    /// Все тексты единым списком (порядок: easy → medium → advanced).
    static let texts: [String] = easyPassages + mediumPassages + advancedPassages

    /// Тексты указанного уровня сложности.
    static func passages(tier: Tier) -> [String] {
        switch tier {
        case .easy: return easyPassages
        case .medium: return mediumPassages
        case .advanced: return advancedPassages
        }
    }

    /// Текст по индексу из общего списка (циклически).
    /// Индекс нормализуется в диапазон `0..<count` — отрицательные значения
    /// не приводят к выходу за границы массива.
    static func text(at index: Int) -> String {
        let count = texts.count
        let normalized = ((index % count) + count) % count
        return texts[normalized]
    }
}
