import Foundation

// MARK: - PacingPhrases
//
// Фразовый материал для упражнения «Темп речи».
//
// Логопедический принцип отбора (Fluency Shaping, фразовый этап):
// - Easy   — короткие фразы 2–3 слова, простые открытые слоги.
// - Medium — фразы 3–4 слова, бытовая лексика, начало коартикуляции.
// - Hard   — фразы 4–6 слов, более длинные слова, удержание темпа на длинной фразе.
//
// Возраст 5–8 лет: только высокочастотный детский лексикон, темы — дом,
// животные, природа. Каждая фраза заранее разбита на слоги вручную, чтобы
// бегунок шёл по корректным речевым слогам (не по формальному делению).

enum PacingPhrases {

    // MARK: - Builder helper

    /// Собирает `PacingPhrase` из массива пар (слово, [слоги]).
    private static func phrase(id: Int, _ pairs: [(String, [String])]) -> PacingPhrase {
        let words = pairs.enumerated().map { wordIndex, pair in
            PacingWord(id: wordIndex, text: pair.0, syllables: pair.1)
        }
        return PacingPhrase(id: id, words: words)
    }

    // MARK: - Easy (2–3 слова)

    static let easyPhrases: [PacingPhrase] = [
        phrase(id: 0, [("кошка", ["кош", "ка"]), ("спит", ["спит"])]),
        phrase(id: 1, [("мама", ["ма", "ма"]), ("дома", ["до", "ма"])]),
        phrase(id: 2, [("рыба", ["ры", "ба"]), ("плывёт", ["плы", "вёт"])]),
        phrase(id: 3, [("луна", ["лу", "на"]), ("светит", ["све", "тит"])]),
        phrase(id: 4, [("вода", ["во", "да"]), ("в", ["в"]), ("реке", ["ре", "ке"])]),
        phrase(id: 5, [("папа", ["па", "па"]), ("читает", ["чи", "та", "ет"])])
    ]

    // MARK: - Medium (3–4 слова)

    static let mediumPhrases: [PacingPhrase] = [
        phrase(id: 100, [("машина", ["ма", "ши", "на"]), ("едет", ["е", "дет"]), ("быстро", ["быст", "ро"])]),
        phrase(id: 101, [("собака", ["со", "ба", "ка"]), ("любит", ["лю", "бит"]), ("играть", ["иг", "рать"])]),
        phrase(id: 102, [("птичка", ["птич", "ка"]), ("поёт", ["по", "ёт"]), ("на", ["на"]), ("ветке", ["вет", "ке"])]),
        phrase(id: 103, [("солнышко", ["сол", "ныш", "ко"]), ("светит", ["све", "тит"]), ("ярко", ["яр", "ко"])])
    ]

    // MARK: - Hard (4–6 слов)

    static let hardPhrases: [PacingPhrase] = [
        phrase(id: 200, [
            ("черепаха", ["че", "ре", "па", "ха"]), ("медленно", ["мед", "лен", "но"]),
            ("идёт", ["и", "дёт"]), ("домой", ["до", "мой"])
        ]),
        phrase(id: 201, [
            ("мальчик", ["маль", "чик"]), ("рисует", ["ри", "су", "ет"]),
            ("красивый", ["кра", "си", "вый"]), ("домик", ["до", "мик"])
        ]),
        phrase(id: 202, [
            ("бабушка", ["ба", "буш", "ка"]), ("печёт", ["пе", "чёт"]),
            ("вкусные", ["вкус", "ны", "е"]), ("пирожки", ["пи", "рож", "ки"])
        ])
    ]

    // MARK: - Public API

    static func phrases(for difficulty: StutteringDifficulty) -> [PacingPhrase] {
        switch difficulty {
        case .easy:   return easyPhrases
        case .medium: return mediumPhrases
        case .hard:   return hardPhrases
        }
    }
}
