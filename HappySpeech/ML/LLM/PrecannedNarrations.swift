        "Ты звезда! Ляля танцует от радости.",
        "Правильно! Ты чувствуешь этот звук!",
        "Идеально! Красивый и чёткий звук."
    ]

    static let repeatAlmost: [String] = [
        "Уже почти, давай вместе ещё разочек.",
