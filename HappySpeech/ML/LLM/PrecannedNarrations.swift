    ]

    // MARK: - Hint generation

    static let hints: [String: [String]] = [
        "narrative_quest": [
            "Открой рот шире и произнеси медленно.",
            "Послушай внимательно — потом повтори.",
            "Не торопись — звук любит спокойствие.",
