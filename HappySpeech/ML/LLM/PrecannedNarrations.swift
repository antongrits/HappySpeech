        ]
    ]

    // MARK: - Public interface

    /// Случайная фраза прогрессии для NarrativeQuest.
    public static func narrativeProgression() -> String {
        narrativeQuestProgressions.randomElement()
            ?? "Идём дальше — нас ждут приключения!"
    }

    /// Feedback-фраза для RepeatAfterModel на основе score (0–100).
    public static func repeatFeedback(score: Int) -> String {
        switch score {
        case 80...:
            return repeatPerfect.randomElement() ?? "Молодец!"
        case 50..<80:
            return repeatAlmost.randomElement() ?? "Почти получилось!"
        default:
            return repeatEncourage.randomElement() ?? "Попробуй ещё раз!"
        }
    }

    /// Контекстная подсказка для игры по gameType.
    public static func hint(for gameType: String) -> String {
        let pool = hints[gameType] ?? hints["general"] ?? ["Попробуй ещё раз!"]
        return pool.randomElement() ?? "Попробуй ещё раз!"
    }
}
