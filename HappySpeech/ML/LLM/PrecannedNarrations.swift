import Foundation

// MARK: - PrecannedNarrations
// ==================================================================================
// Статичный банк заготовленных фраз для kid circuit — fallback когда LLM
// недоступен, недопустимо медленный или возвращает небезопасный контент.
//
// Фразы структурированы по:
//   • gameType: "narrative_quest" | "repeat_after_model" | "hint" | "general"
//   • bucket: "perfect" (score 80–100) | "almost" (50–79) | "encourage" (<50)
//
// Выборка случайная внутри bucket — IntRange.random для воспринимаемого
// разнообразия между сессиями.
// ==================================================================================

