        "Отлично справился! Ляля аплодирует.",
        "Ура! Следующая часть истории ждёт нас."
    ]

    // MARK: - RepeatAfterModel feedback by score bucket

    static let repeatPerfect: [String] = [
        "Получилось чудесно! Ты настоящий звуковой мастер.",
        "Вот это да! Именно так и нужно говорить.",
